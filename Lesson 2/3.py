# Ещё одно, на всякий случай :)
months_dict = {
    'Зима': [1, 2, 12],
    'Весна': [3, 4, 5],
    'Лето': [6, 7, 8],
    'Осень': [9, 10, 11],
}
month_num = int(input('Введите месяц в виде целого числа: '))
while month_num <= 0 or month_num > 12:
    month_num = int(input('Нужно ввести число от 1 до 12! Повторите попытку: '))
for item in months_dict.items():
    if month_num in item[1]:
        print(item[0])
