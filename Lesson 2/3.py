# Решение через list
months_list = [
    ['Январь', 'Зима'],
    ['Февраль', 'Зима'],
    ['Март', 'Весна'],
    ['Апрель', 'Весна'],
    ['Май', 'Весна'],
    ['Июнь', 'Лето'],
    ['Июль', 'Лето'],
    ['Август', 'Лето'],
    ['Сентябрь', 'Осень'],
    ['Октябрь', 'Осень'],
    ['Ноябрь', 'Осень'],
    ['Декабрь', 'Зима']
]
month_num = int(input('Введите месяц в виде целого числа: '))
while month_num <= 0 or month_num > 12:
    month_num = int(input('Нужно ввести число от 1 до 12! Повторите попытку: '))
print(months_list[month_num - 1][1])

# Решение через dict
